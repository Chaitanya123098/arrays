class Arrayed {
    
    public static void printelements(int[] arr){
        for(int i=0;i<arr.length;i++){
            
            System.out.print(arr[i]+" ");
            
        }
    }

    public static void main(String[] args) {
      
       int arr[]={2,4,5,6,7,9};
       printelements(arr);
       
    }
}
